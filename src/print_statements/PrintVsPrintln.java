package print_statements;

public class PrintVsPrintln {
    public static void main(String[] args){

        //println
        //println prints the given text and moves the cursor to the next line
        System.out.println("This is println");
        System.out.println("Abdallah Jaber");
        System.out.println("Today is Sunday");

        //print
        //print prints the given text and keeps
        System.out.print("This is println");
        System.out.print("Abdallah Jaber");
        System.out.print("Today is Sunday");

    }
}
