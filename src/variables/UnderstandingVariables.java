package variables;

public class UnderstandingVariables {
    public static void main(String[] args) {

        //Variable declaration and assignment with an initial value
        int age = 45; //declared variable as 'age' and assigned 45 as value

        System.out.println(age);

        //Variable declaration
        String name; //only declaring the variable, no initial value

        //Variable assignment
        name = "John";

        System.out.println(name); //John

        //Re-assigning
        name = "Mike";

        System.out.println(name); //Mike


        age = 21;

        System.out.println(age);


    }
}
