package practice.variablesPractice;

public class WholeNumbersPractice {

    public static void main(String[] args) {
        byte number1 = 9;
        short number2 = 23;
        int number3 = 2;
        long number4 = 123456789L;

        System.out.println("Number 1 = " + "\"" + number1 + "\"");
        System.out.println("Number 2 = " + "\"" + number2 + "\"");
        System.out.print("Number 3 = " + "\"" + number3 + "\"\n"); //manually adding a new line
        System.out.println("Number 4 = " + "\"" + number4 + "\"");
    }
}
